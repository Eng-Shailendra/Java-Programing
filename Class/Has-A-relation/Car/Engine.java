package Car;

class Engine {
    private String name, type;
    private int hp ;

    public void setHp(int hp) {
        this.hp = hp;
    }
    public void setName(String name) {
        this.name = name;
    }
    public void setType(String type) {
        this.type = type;
    }
    public int getHp() {
        return hp;
    }
    public String getName() {
        return name;
    }
    public String getType() {
        return type;
    }
     
    
}